.model small

.data
ARRAY DB 20 DUP (?)
SIZE DB 00H

msg1 db 10,13,"Enter the size of the array :: $"
msg2 db 10,13,"Enter the array :: $"
msg3 db 10,13,"SORTED ARRAY :: $"

.code
mov ax,@data
mov ds,ax
MOV AH,09
MOV DX,OFFSET msg1
INT 21H

MOV AH,01
INT 21H
SUB AL,30H
MOV CL,AL

MOV SIZE,AL
MOV AH,09
MOV DX,OFFSET msg2
INT 21H
MOV AH,0
MOV SI, 0
MOV CL,SIZE

L1:
MOV DL, 20H
MOV AH, 02H
INT 21H
MOV AH, 01H
INT 21H
SUB AL,30H
MOV ARRAY[SI], AL
INC SI
LOOP L1

MOV CL,SIZE
MOV CH,0
MOV DI,CX

L2:
MOV CL,SIZE
MOV SI,0

L3:
MOV AL,ARRAY[SI]
CMP AL,ARRAY[SI+1]
JL L4
XCHG AL,ARRAY[SI+1]
MOV ARRAY[SI],AL

L4:
INC SI
LOOP L3
DEC DI
JNZ L2

MOV DL,0AH
MOV AH,02H
INT 21H

MOV CL,SIZE
MOV SI,1
MOV AH,09
MOV DX,OFFSET MSG3
INT 21H

L5:
MOV DL,20H
MOV AH,02H
INT 21H
MOV DL,ARRAY[SI]
INC SI
ADD DL,30H
MOV AH,02H
INT 21H
LOOP L5

MOV AH,4CH
INT 21H
END
